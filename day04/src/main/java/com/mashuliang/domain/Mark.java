package com.mashuliang.domain;

public class Mark {
	
	private Integer id;
	
	private String name;
	
	private String prename;
	
	private String pxprename;
	
	private String datea;
	
	private String area;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPrename() {
		return prename;
	}

	public void setPrename(String prename) {
		this.prename = prename;
	}

	public String getPxprename() {
		return pxprename;
	}

	public void setPxprename(String pxprename) {
		this.pxprename = pxprename;
	}

	public String getDatea() {
		return datea;
	}

	public void setDatea(String datea) {
		this.datea = datea;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
	

}
